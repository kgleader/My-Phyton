cells = 1  # Баштапкы клеткалар санын сурайт
days = 7  # Канча күн байкайсың?

counter = 1  # Күндүн номерин баштайбыз (1-күн)

while counter <= days:  # күндөр бүткөнгө чейин цикл иштейт
    cells = cells * 2  # Клеткалар күн сайын эки эсе көбөйөт
    print("Day " + str(counter) + ": " + str(cells))  # Натыйжаны чыгарат
    counter += 1  # Күндү 1ге көбөйтөт (Day 1, Day 2…)
